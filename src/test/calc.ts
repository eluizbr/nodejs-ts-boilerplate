const calc = {
  sum: function(num1: number, num2: number) {
    return num1 + num2;
  },
  sub: function(num1: number, num2: number) {
    return num1 - num2;
  },
  mult: function(num1: number, num2: number) {
    return num1 * num2;
  },
  div: function(num1: number, num2: number) {
    return num1 / num2;
  }
};

export default calc;
